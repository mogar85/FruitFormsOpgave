package dataSet;

public class Form {
	private EFruitType fruitType;
	private int weight;
	private EColor color;
	
	public EFruitType getFruitType() {
		return fruitType;
	}
	
	public void setFruitType(EFruitType fruitType) {
		this.fruitType = fruitType;
	}
	
	public int getWeight() {
		return weight;
	}
	
	public void setWeight(int weight) {
		this.weight = weight;
	}
	
	public EColor getColor() {
		return color;
	}
	
	public void setColor(EColor color) {
		this.color = color;
	}
}